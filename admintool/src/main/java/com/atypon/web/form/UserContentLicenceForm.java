package com.atypon.web.form;

public class UserContentLicenceForm {
    private String username;
    private String contentLicenceId;
    private String startDate;
    private String body;


    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getContentLicenceId() {
        return contentLicenceId;
    }

    public void setContentLicenceId(String contentLicenceId) {
        this.contentLicenceId = contentLicenceId;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
